package com.b07safetyplanapp.models.supportresources;

public class SupportResource {

    private String name;
    private String type;
    private String url;

    public SupportResource() {}


    //Getters
    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getUrl() {
        return url;
    }

}
